#!/usr/bin/env node
// PreToolUse(Bash) hook：拦截 git commit 信息里的「AI 工具署名」落款。
// caseflow 明令禁止任何 AI 署名（git-commit-standards：Author 只填真实提交者）。
// 那是 SKILL 层规则——小改不走 skill 时，宿主/IDE 默认指令可能擅自追加 Co-Authored-By。
// 本 hook 在 commit 落盘前机械扫描提交信息（-m 内联 / heredoc / -F 文件），命中即阻断，
// 与改动大小、是否走 skill 无关。默认 block；CASEFLOW_AI_SIGNATURE_HOOK=warn 仅提示、=off 关闭。
// 局限：编辑器交互式 commit（无 -m/-F）在 PreToolUse 阶段拿不到正文，本 hook 不覆盖（AI 几乎都用 -m）。

const fs = require('fs');
const MODE = (process.env.CASEFLOW_AI_SIGNATURE_HOOK || 'block').toLowerCase();
if (MODE === 'off') process.exit(0);

let raw = '';
process.stdin.setEncoding('utf8');
process.stdin.on('data', (c) => { raw += c; });
process.stdin.on('end', () => {
  let payload; try { payload = JSON.parse(raw); } catch (e) { process.exit(0); }
  if (payload.tool_name !== 'Bash') process.exit(0);
  const cmd = (payload.tool_input && payload.tool_input.command) || '';
  if (!/\bgit\b[^\n]*\bcommit\b/.test(cmd)) process.exit(0);

  let text = cmd;
  for (const m of cmd.matchAll(/(?:-F|--file)[=\s]+(?:"([^"]+)"|'([^']+)'|(\S+))/g)) {
    const f = m[1] || m[2] || m[3];
    if (f) { try { text += '\n' + fs.readFileSync(f, 'utf8'); } catch (e) {} }
  }

  const RULES = [
    { name: 'co-authored-by 指向 AI', re: /(^|\n)\s*co-authored-by:\s*[^\n]*\b(claude|anthropic|copilot|cursor|codex|chatgpt|openai|gemini|bard|gpt)\b/i },
    { name: 'AI 机器邮箱(noreply@anthropic.com)', re: /noreply@anthropic\.com/i },
    { name: '机器人 emoji 落款', re: /🤖/ },
    { name: 'generated with/by AI 落款', re: /(^|\n)\s*(🤖\s*)?generated\s+(with|by)\s+\[?\s*(claude|copilot|cursor|codex|chatgpt|openai|gemini)\b/i },
  ];
  const hit = RULES.find((r) => r.re.test(text));
  if (!hit) process.exit(0);

  process.stderr.write(
    '[caseflow] 提交信息含「AI 工具署名」，caseflow 明令禁止。\n' +
    '  命中：' + hit.name + '\n' +
    '  规则：git-commit-standards —— Author 只填真实提交者；禁止 Co-Authored-By 指向 AI / 机器人 emoji / Generated by … 等任何 AI 署名。\n' +
    '  处置：删除提交信息结尾的 AI 署名行后重新 commit（宿主/IDE 默认追加的签名也要一并去掉）。\n' +
    '  旁路：CASEFLOW_AI_SIGNATURE_HOOK=warn 仅提示 / =off 关闭。\n'
  );
  process.exit(MODE === 'block' ? 2 : 0);
});
